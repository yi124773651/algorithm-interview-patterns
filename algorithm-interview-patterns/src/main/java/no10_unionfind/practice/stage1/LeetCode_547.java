package no10_unionfind.practice.stage1;

/**
 * @ClassName LeetCode_547
 * @Description 省份数量
 * 题目：有 n 个城市，其中一些彼此相连，另一些没有相连。如果城市 a 与城市 b 直接相连，且城市 b 与城市 c 直接相连，
 * 那么城市 a 与城市 c 间接相连。省份是一组直接或间接相连的城市，组内不含其他没有相连的城市。
 * 给你一个 n x n 的矩阵 isConnected，其中 isConnected[i][j] = 1 表示第 i 个城市和第 j 个城市直接相连，返回矩阵中省份的数量。
 * <p>
 * 示例1：
 * 输入：isConnected =
 * [[1,1,0],
 * [1,1,0],
 * [0,0,1]]
 * 输出：2
 * <p>
 * 示例2：
 * 输入：isConnected =
 * [[1,0,0],
 * [0,1,0],
 * [0,0,1]]
 * 输出：3
 * <p>
 * 提示：
 * 1 <= n <= 200
 * n == isConnected.length
 * isConnected[i][j] 为 1 或 0
 * @Author gm
 * @Date 2026/4/2 15:30
 */
public class LeetCode_547 {

    /**
     * 这道题最关键的，不是先背并查集模板，而是先把自己的直觉分析，顺畅地过渡成“集合合并”的语言。
     *
     * 一、如何从你的分析，过渡到标准并查集方法
     * 1. 你的原始想法是对的：
     *    - 每个城市自己先单独算一个区域
     *    - 如果两个城市有连接，就应该归到同一个整体
     *    - 最后问题其实就是：一共剩下几个彼此独立的整体
     *
     * 2. 这句话翻译成并查集语言就是：
     *    - 初始时，每个城市单独属于一个集合
     *    - 看到一条连接边，就把两个集合合并
     *    - 最终有多少个集合，就有多少个省份
     *
     * 3. 所以，并查集并不是一种“全新思路”，而是把“把连在一起的点归成一组”这件事，
     *    用一种高效、标准、可复用的结构表达出来。
     *
     * 二、从“不会”到“会”，最容易卡住的思维障碍
     * 1. 障碍一：总盯着矩阵里的 0 和 1，看不出本质
     *    - 实际上矩阵只是“连接关系的表现形式”
     *    - 题目的本质不是处理矩阵，而是处理“连通块数量”
     *
     * 2. 障碍二：觉得“区域”很抽象，不知道怎么存
     *    - 并查集的做法非常朴素：
     *    - 不直接存整个区域里有哪些点，而是给每个点找一个“代表节点”
     *    - 代表节点相同，就说明它们属于同一个区域
     *
     * 3. 障碍三：一看到合并，就想真的把一堆元素搬来搬去
     *    - 并查集不是“搬家”，而是“认老大”
     *    - 只要把一个集合的代表，挂到另一个集合代表下面，就完成合并了
     *
     * 4. 障碍四：不知道什么时候答案会减少
     *    - 只有当两个城市原本不在同一个集合时，合并才有意义
     *    - 这种有效合并发生一次，省份数才减一
     *    - 如果本来就在一个集合里，再合并一次，答案不变
     *
     * 5. 障碍五：把并查集当成死记硬背模板
     *    - 真正要记的是一句话：
     *    - “初始化各自独立；遇到连接就合并；最后统计集合个数”
     *    - 只要这句想通，模板就不再是硬背，而是自然写出来
     *
     * 三、这题与并查集的对应关系
     * 1. 城市编号 0 ~ n-1，就是并查集里的节点
     * 2. isConnected[i][j] == 1，表示 i 和 j 应该在同一个集合
     * 3. 对角线 isConnected[i][i] == 1，只表示自己和自己连通，不影响省份数
     * 4. 最后剩余的集合数量 sets，就是答案
     *
     * 四、并查集通用模板应该怎么理解
     * 1. parent[x]：节点 x 当前指向的父节点
     * 2. find(x)：一路向上找，直到找到集合代表节点
     * 3. union(a, b)：把 a 和 b 所在集合合并
     * 4. sets：当前还剩多少个集合
     * 5. size[root]：记录某个代表节点所在集合大小，用于“小挂大”优化
     *
     * 五、并查集通用模板伪代码
     * 1. 初始化：
     *    - parent[i] = i
     *    - size[i] = 1
     *    - sets = n
     * 2. 查找代表：
     *    - 如果 parent[x] != x，递归找代表，并顺手把路径压缩
     * 3. 合并集合：
     *    - 先找两个点的代表 rootA、rootB
     *    - 如果代表相同，说明已经连通，直接返回
     *    - 如果不同，则把小集合挂到大集合上
     *    - sets--
     *
     * 六、以后遇到题目，如何判断能不能用并查集
     * 只要题目在问下面这类东西，就要想到并查集：
     * 1. 有多少个连通块 / 区域 / 省份 / 集团
     * 2. 两个点是否属于同一组
     * 3. 动态地把两个对象合并成一组
     * 4. 本质是“分组”和“连通性”问题
     */
    public static int unionFind(int[][] isConnected) {
        int cityCount = isConnected.length;
        UnionFind unionFind = new UnionFind(cityCount);

        // 你的原始理解可以这样过渡：
        // 1. 一开始，每个城市都看成一个独立省份，所以一共有 cityCount 个集合。
        // 2. 如果发现两个城市直接相连，那么它们就不该再分开算，而应合并为同一个整体。
        // 3. 把所有“应该合并”的城市都处理完后，剩下几个集合，就有几个省份。
        //
        // 这里只遍历右上半区：
        // - 因为 isConnected[i][j] 和 isConnected[j][i] 表示的是同一条连接关系
        // - 避免重复处理
        // - 对角线上的 1 只是自己和自己连通，不会产生新的合并
        for (int i = 0; i < cityCount; i++) {
            for (int j = i + 1; j < cityCount; j++) {
                if (isConnected[i][j] == 1) {
                    unionFind.union(i, j);
                }
            }
        }

        return unionFind.getSets();
    }

    /**
     * 并查集通用模板
     *
     * 一、你可以把它记成三个核心动作：
     * 1. 初始化：每个点先各自为战
     * 2. 查找：判断两个点是不是同一个老大带领
     * 3. 合并：如果不是同一个老大，就把两个集合并起来
     *
     * 二、模板适用场景
     * 1. 无向图连通块统计
     * 2. 岛屿数量类问题
     * 3. 账户合并、朋友圈、网络连通性
     * 4. “属于同一组吗”这类判定问题
     */
    private static class UnionFind {
        /**
         * parent[i] 表示 i 的父节点是谁。
         *
         * 如果 parent[i] == i，说明 i 就是当前集合的代表节点，也可以理解为“这个集合的老大”。
         */
        private final int[] parent;

        /**
         * size[i] 只在 i 是代表节点时有意义，表示以 i 为代表的集合大小。
         *
         * 作用：合并时让小集合挂到大集合上，避免树太高，提升查找效率。
         */
        private final int[] size;

        /**
         * sets 表示当前集合数量。
         *
         * 在这道题里，它就等价于“当前省份数量”。
         */
        private int sets;

        /**
         * 初始化模板：
         * 1. 每个节点的父节点先指向自己
         * 2. 每个节点单独成一个集合，大小为 1
         * 3. 一共有 n 个集合
         */
        public UnionFind(int n) {
            this.parent = new int[n];
            this.size = new int[n];
            this.sets = n;
            for (int i = 0; i < n; i++) {
                parent[i] = i;
                size[i] = 1;
            }
        }

        /**
         * 查找模板：返回当前节点所在集合的代表节点。
         *
         * 路径压缩的含义：
         * - 查找过程中，顺手把沿途节点直接挂到代表节点下面
         * - 这样下次再查会更快
         *
         * 通用记忆：
         * - 先向上找老大
         * - 找到后，让中间所有人都直接认这个老大
         */
        public int find(int node) {
            if (parent[node] != node) {
                parent[node] = find(parent[node]);
            }
            return parent[node];
        }

        /**
         * 合并模板：把两个节点所在集合合并。
         *
         * 通用步骤：
         * 1. 先分别找到两个节点的代表节点
         * 2. 如果代表相同，说明本来就在同一集合，无需处理
         * 3. 如果代表不同，则执行集合合并
         * 4. 合并成功一次，集合数减一
         *
         * 这里使用“小挂大”优化：
         * - 小集合挂到大集合上
         * - 可以降低树高，让后续 find 更快
         */
        public void union(int a, int b) {
            int rootA = find(a);
            int rootB = find(b);
            if (rootA == rootB) {
                return;
            }

            if (size[rootA] < size[rootB]) {  //保证 rootA >= rootB 统一处理
                int temp = rootA;
                rootA = rootB;
                rootB = temp;
            }

            parent[rootB] = rootA;
            size[rootA] += size[rootB];
            sets--;
        }

        /**
         * 返回当前集合数量。
         *
         * 在本题里，集合数量 = 省份数量。
         */
        public int getSets() {
            return sets;
        }
    }

    public static void main(String[] args) {
        System.out.println(unionFind(new int[][]{{1, 1, 0}, {1, 1, 0}, {0, 0, 1}})); // 2
        System.out.println(unionFind(new int[][]{{1, 0, 0}, {0, 1, 0}, {0, 0, 1}})); // 3
    }
}
