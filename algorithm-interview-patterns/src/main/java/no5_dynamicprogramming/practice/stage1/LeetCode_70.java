package no5_dynamicprogramming.practice.stage1;

/**
 * @ClassName LeetCode_70
 * @Description 爬楼梯
 * 题目：假设你正在爬楼梯。需要 n 阶你才能到达楼顶。每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶？
 * 示例1：输入：n = 2 输出：2 解释：1+1, 2
 * 示例2：输入：n = 3 输出：3 解释：1+1+1, 1+2, 2+1
 * 提示：1 <= n <= 45
 * @Author gm
 * @Date 2026/4/2 15:30
 */
public class LeetCode_70 {

    public static void main(String[] args) {
        System.out.println(dp(2)); // 2
        System.out.println(dp(3)); // 3
    }

    /**
     * DP解题的4个步骤
     * 1. 定义状态: dp[i] 代表什么？
     * 2. 状态转移方程: dp[i] 由哪些 dp[j] 推导而来？
     * 3. 初始条件: dp[0] 或 dp[0][0] 是什么？
     * 4. 遍历顺序: 从小到大？从大到小？
     * <p>
     * 本题分析：
     * 1. 定义状态：dp[i] 代表什么？
     * dp[i] = 爬到第 i 阶楼梯的方法数。
     * <p>
     * 2. 状态转移方程：dp[i] 由哪些推导？
     * 要到达第 i 阶，只有两种方式：
     * - 从第 i-1 阶爬 1 步上来
     * - 从第 i-2 阶爬 2 步上来
     * 所以：dp[i] = dp[i-1] + dp[i-2]
     * <p>
     * 3. 初始条件
     * - dp[1] = 1 — 到第 1 阶只有 1 种方法（爬 1 步）
     * - dp[2] = 2 — 到第 2 阶有 2 种方法（1+1 或 2）
     * 4. 遍历顺序
     * 从小到大，i = 3 遍历到 n。因为 dp[i] 依赖 dp[i-1] 和 dp[i-2]，必须先算出小的才能算大的。
     * <p>
     * --------------------------------------------------------
     * 维度	   简单题（如本题）	     复杂题
     * 状态	   一维 dp[i]	         二维 dp[i][j] 甚至更高
     * 转移	   固定几项相加	         可能需要循环求最值
     * 遍历	   单纯从小到大	         可能需要特殊顺序
     * -------------------------------------------------------------
     * 核心永远是这四步：定义状态 → 找转移方程 → 确定初始值 → 确定遍历顺序。
     **/


    /**
     * DP 的核心思想就是把算过的结果存起来，避免重复计算。
     * 用数组从小到大递推
     * @param n
     * @return
     */
    public static int dp(int n) {
        if (n <= 2) return n;
        int[] dp = new int[n + 1];
        dp[1] = 1;
        dp[2] = 2;
        for (int i = 3; i <= n; i++) {
            dp[i] = dp[i - 1] + dp[i - 2];
        }
        return dp[n];
    }


    /**
     * 逻辑正确，但这不是DP，而是纯递归  当n很大时就很慢
     * 核心问题：大量重复计算。
     * 比如算 dp(5) 的调用过程：
     * dp(5)
     * ├── dp(4)
     * │   ├── dp(3)
     * │   │   ├── dp(2) ✅
     * │   │   └── dp(1) ✅
     * │   └── dp(2) ✅
     * └── dp(3)           ← 和上面重复了！
     * ├── dp(2) ✅
     * └── dp(1) ✅
     * <p>
     * dp(3) 被算了 2 次，n 越大重复越多，时间复杂度是 O(2^n)，当 n=45（题目上限）时会非常慢。
     *
     * @param n
     * @return
     */
    public static int notDp(int n) {
        // TODO
        if (n == 1) return 1;
        if (n == 2) return 2;
        return notDp(n - 1) + notDp(n - 2);
    }
}
